							<p>JAKARTA – Pendidikan dan akhlak bangsa menjadi isu utama dalamRapat Kerja Nasional Badan Kontak Majelis Taklim (Rakernas BKMT) yang dihadiri 350 pengurus dari seluruh Indonesia. Rakernas dimulai sejak Kamis dan berakhir Sabtu (22/2) ini. Ketua Umum BKMT Tutty Alawiyah mengatakan lembaganya memang sengaja menyoroti pendidikan. Sebab, pendidikan Indonesia tertinggal dibandingkan negara Asia Tenggara lainnya. “Bahkan, kualitas pekerja Indonesia kalah daripada pekerja Filipina,” katanya, Jumat (21/2). Para pekerja Indonesia kemampuannya kurang dan akhirnya banyak yang harus menerima perlakuan buruk. BKMT juga ingin menunjukkan Islam tak pernah membedakan laki-laki dan perempuan dalam urusan mencari ilmu dan berkontribusi di masyarakat. BKMT mempersilakan pula anggotanya yang ingin masuk ke kancah politik. “Secara individu kalau memang terpanggil, silakan saja, tetapi sebagai organisasi kami bersikap netral,” ujar Tutty. Urusan politik jangan sampai lepas ke tangan orang lain, umat Islam harus berperan. </p>

							<p>Menurut Tutty, sekarang ini moral bangsa semakin terkikis. Karena itu, penguatan ketahanan keluarga menjadi kunci perbaikan akhlak gerenasi muda. Ia mendesak para Muslimah membagi waktunya dalam tiga porsi.Sepertiga untuk diri sendiri, sepertiga untuk keluarga, dan sepertiga lainnya untuk masyarakat. “Jangan sampai anak-anak bergaul tanpa sepengetahuan orang tuanya. Orang tua harus lebih memperhatikan siapa teman-teman anak mereka,” ujar Tutty. Ia miris dengan maraknya acara musik di televisi yang tayang setiap hari. Sebab, pengaruhnya bukan hanya terhadap anak-anak, tetapi juga kaum ibu. Kondisi semacam ini bakal melemahkan ketahanan dalam keluarga.</p>
	

